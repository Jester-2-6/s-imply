def resolve_gate_types(
    node_ids: torch.Tensor, files: List[str], device: torch.device
) -> torch.Tensor:
    """
    Resolve gate types for a batch of node IDs using cached circuit parsing.
    Returns [B, P, L] tensor of gate types.
    """
    gates = torch.full_like(node_ids, -1, device=device)
    # This is slow if batch size is large and files are diverse;
    # meant as fallback if gate_types not in batch.

    # Group by file to minimize parsing
    file_map = {}
    for b, f in enumerate(files):
        if f not in file_map:
            file_map[f] = []
        file_map[f].append(b)

    for f, indices in file_map.items():
        circuit = ReconvergentPathsDataset._load_circuit_cached(f)
        for b in indices:
            # node_ids[b] is [P, L]
            # iterate and map
            # Vectorized mapping is hard without a dense map tensor for the whole circuit,
            # which we don't have readily available in a tensor form here.
            # So we iterate. This is slow!

            # Optimization: Move scalar loop to CPU
            nb = node_ids[b].cpu()
            gb = gates[b].cpu()

            # We can map using a lookup
            # But simplest is loop.
            P, L = nb.shape
            for p in range(P):
                for length in range(L):
                    nid = int(nb[p, length].item())
                    if nid > 0 and nid < len(circuit):
                        gb[p, length] = int(circuit[nid].type)

            gates[b] = gb.to(device)

    return gates


def generate_constraints(
    node_ids: torch.Tensor, files: List[str], prob: float = 0.5
) -> Tuple[torch.Tensor, torch.Tensor]:
    """
    Generate random constraints for training.
    Returns:
        mask: [B, P, L] bool, True where constraint is applied
        vals: [B, P, L] int (0 or 1), the target value
    """
    if prob <= 0:
        return (
            torch.zeros_like(node_ids, dtype=torch.bool),
            torch.zeros_like(node_ids, dtype=torch.long),
        )

    # Random mask
    mask = (torch.rand_like(node_ids, dtype=torch.float) < prob) & (node_ids > 0)

    # Random values (0 or 1)
    # In a real constrained task, we might want *consistent* values,
    # but for "Constrained Path Training" to robustify the model,
    # random constraints force the model to adapt.
    vals = torch.randint_like(node_ids, 0, 2)

    return mask, vals


def _generate_anchor(
    node_ids: torch.Tensor, attn_mask: torch.Tensor, files: List[str], prefer_value: int = 1
) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor]:
    """
    Batch version of anchor generation.
    Returns (anchor_p, anchor_l, anchor_v, solvability) tensors.
    """
    B = node_ids.shape[0]
    dev = node_ids.device
    ap = torch.zeros(B, dtype=torch.long, device=dev)
    al = torch.zeros(B, dtype=torch.long, device=dev)
    av = torch.zeros(B, dtype=torch.long, device=dev)
    sv = torch.zeros(B, dtype=torch.long, device=dev)

    # We need to loop. Solver is CPU-bound.
    for b in range(B):
        # We need info dict for solver?
        # Wait, _gen_anchor_for_sample takes pair_info.
        # But here we don't have pair_info!
        # If we are in the training loop, we might have lost the pair_info
        # unless it was passed through collate.
        # ReconvergentPathsDataset.__getitem__ has access to it.
        # If we are calling this from train.py, we only have tensors and files.
        # We CANNOT run the exact same logic without pair_info
        # (which contains reconvergence details)

        # The reconvergence node is always the LAST valid position on every path
        # (paths are ordered [stem -> intermediate -> reconv]). We can extract it
        # from the attn_mask alone without pair_info or a solver call.
        path_len = int(attn_mask[b, 0].long().sum().item())
        if path_len >= 1:
            ap[b] = 0                    # path 0 (all paths share the same terminal)
            al[b] = path_len - 1         # last valid position
            # av[b] is already set to prefer_value above; keep it.
        else:
            ap[b] = -1
            al[b] = -1

    return ap, al, av, sv


def _inject_anchor_into_embeddings(
    paths: torch.Tensor,
    anchor_p: torch.Tensor,
    anchor_l: torch.Tensor,
    anchor_v: torch.Tensor,
    enable: bool = True,
) -> torch.Tensor:
    """
    Inject anchor one-hot vector into the last 3 channels of embeddings.
    """
    if not enable:
        return paths

    B, P, L, D = paths.shape
    if D < 3:
        return paths

    # We need to scatter.
    # Iterate for simplicity since B is small-ish (128).
    # Vectorized is better.

    # paths[b, p, l, -3:] = one_hot

    # Filter valid anchors
    valid = (anchor_p >= 0) & (anchor_l >= 0)
    if not valid.any():
        return paths

    # Creating a mask index is tricky with 4D tensor.
    # Clone to avoid in-place issues if needed?
    out = paths.clone()

    # Loop over valid indices
    idxs = torch.nonzero(valid).squeeze(1)
    for b in idxs:
        p = anchor_p[b].item()
        l_val = anchor_l[b].item()
        v = anchor_v[b].item()

        # One-hot: 0 -> [1,0,0], 1 -> [0,1,0], X -> [0,0,1]
        # X is default.
        if v == 0:
            vec = torch.tensor([1.0, 0.0, 0.0], device=paths.device)
        elif v == 1:
            vec = torch.tensor([0.0, 1.0, 0.0], device=paths.device)
        else:
            vec = torch.tensor([0.0, 0.0, 1.0], device=paths.device)

        out[b, p, l_val, D - 3 :] = vec

    return out
